// Function expression for divide
const divide = function(a, b) {
    return a / b;
  };
  
  // Arrow function for square
  const square = x => x * x;
  
  // Arrow function for add
  const add = (a, b) => a + b;
  

console.log(divide(2000, 100)); 
console.log(square(2)); 
console.log(add(3, 4)); 